// Question 2: Find the Sum of All Numbers in an Array
// ========================================

const array2 = [10, 20, 30, 40, 50];

let sum = 0;

for (let i = 0; i < array2.length; i++) {
    sum += array2[i];
}

console.log("Sum of Array:", sum);

// Expected Output:
// Sum of Array: 150